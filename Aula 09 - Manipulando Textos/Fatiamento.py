frase = 'Curso em vídeo Python'
print(frase[3])
# Mostra o caractér 3 (lembre-se que a contagem começa do zero)
print(frase[9:13])
# Primeiro incluso, último não
print(frase[9:21:2])
# O último 2 equivale a "pulos"
print(frase[:5])
# Inicio omitido = começa do início
print(frase[15:])
# Fim omitido = termina no último
print(frase[9::3])
# Mostra do nove até o fim pulando de 3 em 3
